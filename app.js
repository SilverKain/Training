function updateSetIndicators() {
    const exercise = currentTraining.exercises[currentExerciseIndex];
    const tracker = $('setTracker');
    tracker.innerHTML = '';

    for (let i = 0; i < exercise.sets; i++) {
        const indicator = document.createElement('div');
        indicator.className = 'set-indicator';
        if (i < currentSet) {
            indicator.classList.add('completed');
        } else if (i === currentSet) {
            indicator.classList.add('current');
        }
        indicator.textContent = i + 1;
        tracker.appendChild(indicator);
    }
}

// ===== Complete Set =====
function completeSet() {
    const exercise = currentTraining.exercises[currentExerciseIndex];

    // –î–ª—è —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π —Å –≤—Ä–µ–º–µ–Ω–µ–º ‚Äî —Å–Ω–∞—á–∞–ª–∞ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–∞–π–º–µ—Ä –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è
    if ($('completeSetBtn').dataset.timedMode === 'start') {
        $('completeSetBtn').style.display = 'none';
        showExerciseTimer(parseExerciseDuration(exercise));
        return;
    }

    currentSet++;
    updateSetIndicators();

    if (currentSet < exercise.sets) {
        // –ï—â—ë –µ—Å—Ç—å –ø–æ–¥—Ö–æ–¥—ã ‚Äî –∑–∞–ø—É—Å–∫–∞–µ–º —Ç–∞–π–º–µ—Ä –æ—Ç–¥—ã—Ö–∞ –º–µ–∂–¥—É –ø–æ–¥—Ö–æ–¥–∞–º–∏
        $('completeSetBtn').style.display = 'none';
        showRestTimer(exercise.rest, 'set-rest');
    } else {
        // –í—Å–µ –ø–æ–¥—Ö–æ–¥—ã –≤—ã–ø–æ–ª–Ω–µ–Ω—ã ‚Äî —Å–æ—Ö—Ä–∞–Ω—è–µ–º —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ
        markExerciseDone(currentTrainingWeek, currentTrainingIndex, currentTraining.exercises[currentExerciseIndex]);
        updateProgressStats();
        $('completeSetBtn').style.display = 'none';

        if (currentExerciseIndex < currentTraining.exercises.length - 1) {
            // –ï—Å—Ç—å –µ—â—ë —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è ‚Äî –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –∫–Ω–æ–ø–∫—É "–°–ª–µ–¥—É—é—â–µ–µ"
            $('nextExerciseBtn').style.display = 'block';
        } else {
            // –ü–æ—Å–ª–µ–¥–Ω–µ–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ ‚Äî –∑–∞–≤–µ—Ä—à–∞–µ–º —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫—É
            completeWorkout();
        }
    }
}

// ===== After Exercise Timer (timed sets) =====
function afterExerciseSet() {
    const exercise = currentTraining.exercises[currentExerciseIndex];

    currentSet++;
    updateSetIndicators();

    if (currentSet < exercise.sets) {
        showRestTimer(exercise.rest, 'set-rest');
    } else {
        markExerciseDone(currentTrainingWeek, currentTrainingIndex, currentTraining.exercises[currentExerciseIndex]);
        updateProgressStats();

        if (currentExerciseIndex < currentTraining.exercises.length - 1) {
            $('nextExerciseBtn').style.display = 'block';
        } else {
            completeWorkout();
        }
    }
}

// ===== Next Exercise =====
function nextExercise() {
    const prevExercise = currentTraining.exercises[currentExerciseIndex];
    currentExerciseIndex++;
    currentSet = 0;

    $('completeSetBtn').style.display = 'none';
    $('nextExerciseBtn').style.display = 'none';

    if (currentExerciseIndex < currentTraining.exercises.length) {
        const nextEx = currentTraining.exercises[currentExerciseIndex];
        const prevIsSuperset = prevExercise.name.startsWith('–°—É–ø–µ—Ä—Å–µ—Ç:');
        const nextIsSuperset = nextEx.name.startsWith('–°—É–ø–µ—Ä—Å–µ—Ç:');

        if (prevIsSuperset && nextIsSuperset) {
            // –°—É–ø–µ—Ä—Å–µ—Ç ‚Äî –±–µ–∑ –æ—Ç–¥—ã—Ö–∞, —Å—Ä–∞–∑—É —Å–ª–µ–¥—É—é—â–µ–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ
            showExerciseUI();
        } else {
            showRestTimer(60, 'exercise-rest');
        }
    } else {
        completeWorkout();
    }
}

