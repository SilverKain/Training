function updateTimerDisplay() {
    const m = Math.floor(timerSeconds / 60);
    const s = timerSeconds % 60;
    $('timerDisplay').textContent =
        `${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}`;
}

function startTimer() {
    if (!timerInterval && timerSeconds > 0) {
        timerInterval = setInterval(() => {
            timerSeconds--;
            updateTimerDisplay();
            if (timerSeconds <= 0) {
                onTimerEnd();
            }
        }, 1000);
        $('startTimerBtn').classList.add('hidden');
        $('pauseTimerBtn').classList.remove('hidden');
    }
}

function pauseTimer() {
    if (timerInterval) {
        clearInterval(timerInterval);
        timerInterval = null;
        $('startTimerBtn').classList.remove('hidden');
        $('pauseTimerBtn').classList.add('hidden');
    }
}

function hideTimer() {
    clearInterval(timerInterval);
    timerInterval = null;

    const section = $('timerSection');
    section.classList.add('hidden');
    section.classList.remove('resting', 'exercising');

    $('startTimerBtn').classList.remove('hidden');
    $('pauseTimerBtn').classList.add('hidden');
}

function onTimerEnd() {
    const prevType = timerType;
    timerType = null;
    hideTimer();

    if (prevType === 'exercise') {
        // –¢–∞–π–º–µ—Ä –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è –∑–∞–≤–µ—Ä—à—ë–Ω ‚Äî –∑–∞—Å—á–∏—Ç—ã–≤–∞–µ–º –ø–æ–¥—Ö–æ–¥
        afterExerciseSet();
    } else if (prevType === 'set-rest') {
        // –ü–æ–∫–∞–∑–∞—Ç—å –∫–Ω–æ–ø–∫—É —Å–ª–µ–¥—É—é—â–µ–≥–æ –ø–æ–¥—Ö–æ–¥–∞
        $('completeSetBtn').style.display = 'block';
        $('completeSetBtn').disabled = false;
    } else if (prevType === 'exercise-rest') {
        // –ü–µ—Ä–µ–π—Ç–∏ –∫ —Å–ª–µ–¥—É—é—â–µ–º—É —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—é
        showExerciseUI();
    }
}

function skipRest() {
    onTimerEnd();
}

