// ===== Timed Exercise Helpers =====
function isTimedExercise(exercise) {
    return /\d+\s*—Å–µ–∫/i.test(exercise.reps);
}

function parseExerciseDuration(exercise) {
    const rangeMatch = exercise.reps.match(/(\d+)\s*-\s*(\d+)\s*—Å–µ–∫/i);
    if (rangeMatch) {
        return parseInt(rangeMatch[2]); // –≤–µ—Ä—Ö–Ω—è—è –≥—Ä–∞–Ω–∏—Ü–∞ –¥–∏–∞–ø–∞–∑–æ–Ω–∞
    }
    const singleMatch = exercise.reps.match(/(\d+)\s*—Å–µ–∫/i);
    if (singleMatch) {
        return parseInt(singleMatch[1]);
    }
    return 30;
}

